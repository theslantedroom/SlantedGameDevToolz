export function getRandomDarkColor() {
	const letters = "0123456789ABCDEF";
	let color = "#";
	for (let i = 0; i < 3; i++) {
		const index = Math.floor(Math.random() * letters.length);
		const letter = letters[index];
		color += letter + letter;
	}
	return color;
}
